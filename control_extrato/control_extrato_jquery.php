function scEventControl_active(iSeqRow) {
  if (scEventControl_data["datainicial" + iSeqRow]["blur"]) {
    return true;
  }
  if (scEventControl_data["datainicial" + iSeqRow]["change"]) {
    return true;
  }
  if (scEventControl_data["datafinal" + iSeqRow]["blur"]) {
    return true;
  }
  if (scEventControl_data["datafinal" + iSeqRow]["change"]) {
    return true;
  }
  if (scEventControl_data["tipo" + iSeqRow]["blur"]) {
    return true;
  }
  if (scEventControl_data["tipo" + iSeqRow]["change"]) {
    return true;
