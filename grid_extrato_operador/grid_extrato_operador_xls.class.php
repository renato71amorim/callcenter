      $this->arr_span   = array();

      if (isset($_SESSION['sc_session'][$this->Ini->sc_page]['grid_extrato_operador']['embutida_label']) && $_SESSION['sc_session'][$this->Ini->sc_page]['grid_extrato_operador']['embutida_label'])
